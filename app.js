// ============================================================

function loadState() {
    try {
        const data = JSON.parse(localStorage.getItem('humanLibrary'));
        if (data) Object.assign(state, data);
    } catch (e) {}
}

function saveState() {
    localStorage.setItem('humanLibrary', JSON.stringify(state));
}

