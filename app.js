//  4. РЕНДЕРИНГ
// ============================================================

const libraryView = document.getElementById('libraryView');
const readerView = document.getElementById('readerView');
const bookGrid = document.getElementById('bookGrid');

function renderLibrary() {
    bookGrid.innerHTML = '';
    let readCount = 0;

    const filtered = state.filter === 'all'
        ? BOOKS
        : BOOKS.filter(b => b.tags.includes(state.filter));

    filtered.forEach(book => {
        const progress = state.progress[book.id] || 0;
        const total = book.chapters.length;
        const percent = total > 0 ? Math.round((progress / total) * 100) : 0;
        if (percent >= 100) readCount++;

        const card = document.createElement('div');
        card.className = 'book-card';
        if (state.filter !== 'all' && !book.tags.includes(state.filter)) {
            card.classList.add('hidden');
        }

        const tagHTML = book.tags.map(t => `<span class="tag">${t}</span>`).join('');

        card.innerHTML = `
            <div class="book-cover">${book.emoji}</div>
            <h3>${book.title}</h3>
            <p class="author">${book.author}</p>
            <div class="tags">${tagHTML}</div>
            <p class="desc">${book.desc}</p>
            <div class="progress-mini"><div class="fill" style="width:${percent}%;"></div></div>
            <button class="read-btn" data-id="${book.id}">
                ${percent === 0 ? '📖 Начать читать' : percent === 100 ? '✅ Прочитана' : `📖 Продолжить (${percent}%)`}
            </button>
        `;
        card.querySelector('.read-btn').addEventListener('click', () => openBook(book.id));
        bookGrid.appendChild(card);
    });

    // Счётчик прочитанных
    let totalRead = 0;
    BOOKS.forEach(b => {
        const p = state.progress[b.id] || 0;
        if (p >= b.chapters.length) totalRead++;
    });
    document.getElementById('booksRead').textContent = `${totalRead} книг`;

    let level = '🗿 Искатель';
    if (totalRead >= 10) level = '🏆 Просветленный';
    else if (totalRead >= 7) level = '🧙 Мудрец';
    else if (totalRead >= 4) level = '⚔️ Воин духа';
    else if (totalRead >= 2) level = '📖 Читатель';
    document.getElementById('levelBadge').textContent = level;

    // Обновить кнопки фильтров
    document.querySelectorAll('.filter-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.filter === state.filter);
    });
}

