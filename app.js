function openBook(bookId) {
    state.currentBookId = bookId;
    const book = BOOKS.find(b => b.id === bookId);
    if (!book) return;
    state.currentChapter = state.progress[bookId] || 0;
    if (state.currentChapter >= book.chapters.length) state.currentChapter = book.chapters.length - 1;
    showReader(book);
}

function showReader(book) {
    document.getElementById('readerTitle').textContent = book.title;
    document.getElementById('readerAuthor').textContent = book.author;
    renderChapter(book);
    libraryView.classList.remove('active');
    readerView.classList.add('active');
}

function renderChapter(book) {
    const chapters = book.chapters;
    const idx = state.currentChapter;

    if (idx >= chapters.length) {
        document.getElementById('readerContent').innerHTML = `
            <h3>🎉 Книга "${book.title}" прочитана!</h3>
            <p>Поздравляю! Ты завершил трансформацию через эту книгу.</p>
            <p>Начни следующую или перечитай заново.</p>
        `;
        document.getElementById('chapterIndicator').textContent = '✅ Завершено';
        document.getElementById('nextChapter').style.display = 'none';
        document.getElementById('prevChapter').style.display = 'none';
        updateProgress(book.id, idx);
        return;
    }

    const chapterTitle = chapters[idx];
    const chapterContent = book.content[chapterTitle] || 'Содержание этой главы.';

    document.getElementById('readerContent').innerHTML = `
        <h3>${chapterTitle}</h3>
        <p>${chapterContent}</p>
    `;
    document.getElementById('chapterIndicator').textContent = `${idx + 1} / ${chapters.length}`;
    document.getElementById('nextChapter').style.display = 'inline-block';
    document.getElementById('prevChapter').style.display = 'inline-block';

    const percent = Math.round(((idx + 1) / chapters.length) * 100);
    document.getElementById('readerProgress').style.width = percent + '%';
    document.getElementById('progressPercent').textContent = percent + '%';

    updateProgress(book.id, idx);
    renderQuotes(book.id);
}

function updateProgress(bookId, chapterIdx) {
    const current = state.progress[bookId] || 0;
    if (current < chapterIdx + 1) {
        state.progress[bookId] = chapterIdx + 1;
        saveState();
        renderLibrary();
    }
}

