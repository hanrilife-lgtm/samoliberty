function renderQuotes(bookId) {
    const list = document.getElementById('quoteList');
    const quotes = state.quotes.filter(q => q.bookId === bookId);
    list.innerHTML = '';
    if (quotes.length === 0) {
        list.innerHTML = '<li class="empty">Выдели текст и добавь как цитату</li>';
        return;
    }
    quotes.forEach(q => {
        const li = document.createElement('li');
        li.textContent = q.text;
        list.appendChild(li);
    });
}

document.getElementById('addQuoteBtn').addEventListener('click', () => {
    const selection = window.getSelection().toString().trim();
    if (!selection) {
        alert('Сначала выдели текст мышкой в книге!');
        return;
    }
    state.quotes.push({ bookId: state.currentBookId, text: selection });
    saveState();
    renderQuotes(state.currentBookId);
    alert('📌 Цитата сохранена!');
});

